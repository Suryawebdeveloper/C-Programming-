 #include<stdio.h>
 #include<conio.h>
 void main() {
              int a=12,b=7;
              printf("a =%d\n",a);
              printf("b =%d\n\n",b);
              
              // And Gate or Bitwise And
              printf("a&b =%d\n",a&b);
              
              // Or Gate or Bitwise Or
              printf("a|b =%d\n",a|b);
              
              // XOR Gate
              printf("a^b =%d\n",a^b);
              
              // Compliment or Bitwise Not
              printf("~a =%d\n",~a);
              printf("~b =%d\n\n",~b);
              
              // Right Shift
              printf("a>>2 =%d\n",a>>2);
              
              // Left Shift
              printf("a<<3 =%d\n",a<<3);
 getch();
 }